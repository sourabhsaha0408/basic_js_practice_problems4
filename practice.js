//Write a JavaScript program that displays the largest integer among two integers.

const firstInteger = 3;
const secondInteger = 3;

if (firstInteger > secondInteger) {
  console.log(`The largest integer is ${firstInteger}`);
} else if (firstInteger < secondInteger) {
  console.log(`The largest integer is ${secondInteger}`);
} else {
  console.log(`Both are of equal values`);
}
